// Addition + , subtraction - , multiplication * ,/ division , tilda ~/ absract this give whole number , module % get the reminder of an integer division  //
void main(){
  int num1 = 2 ;
  int num2 = 8 ;
  print(num1+num2);
  print(num1-num2);
  print(num1*num2);
  print(num1/num2);
  print(num1~/num2);
  print(num1%num2);
}